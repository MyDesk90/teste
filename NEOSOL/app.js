let currentLang = "pt";

// Initialize page
document.addEventListener("DOMContentLoaded", () => {
  // Setup Language Selector Click Handlers
  const ptBtn = document.getElementById("lang-pt");
  const enBtn = document.getElementById("lang-en");
  
  if (ptBtn && enBtn) {
    ptBtn.addEventListener("click", () => setLanguage("pt"));
    enBtn.addEventListener("click", () => setLanguage("en"));
  }

  // Setup Solution Calculator Submit handler
  const calcBtn = document.getElementById("calc-submit-btn");
  if (calcBtn) {
    calcBtn.addEventListener("click", runCalculator);
  }

  // Setup Contact Form Submit Handler
  const contactForm = document.getElementById("contact-form");
  if (contactForm) {
    contactForm.addEventListener("submit", handleFormSubmit);
  }

  // Initial Page Update
  updateTranslations();
});

// Switch language function
function setLanguage(lang) {
  currentLang = lang;
  
  // Update Active Class on Buttons
  const ptBtn = document.getElementById("lang-pt");
  const enBtn = document.getElementById("lang-en");
  
  if (ptBtn && enBtn) {
    if (lang === "pt") {
      ptBtn.classList.add("active");
      enBtn.classList.remove("active");
    } else {
      enBtn.classList.add("active");
      ptBtn.classList.remove("active");
    }
  }

  // Re-translate page elements
  updateTranslations();
  
  // Re-run calculator if results are active to update translation
  const resultCard = document.getElementById("calc-result-card");
  if (resultCard && !resultCard.classList.contains("hidden")) {
    runCalculator();
  }
}

// Update text of DOM elements with data-i18n
function updateTranslations() {
  const elements = document.querySelectorAll("[data-i18n]");
  elements.forEach(el => {
    const key = el.getAttribute("data-i18n");
    if (translations[currentLang][key]) {
      if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
        el.placeholder = translations[currentLang][key];
      } else {
        el.textContent = translations[currentLang][key];
      }
    }
  });
}

// Calculator Logic
function runCalculator() {
  const sizeSelect = document.getElementById("calc-size");
  const challengeSelect = document.getElementById("calc-challenge");
  
  if (!sizeSelect || !challengeSelect) return;

  const size = sizeSelect.value;
  const challenge = challengeSelect.value;

  let recommendationKey = "";
  
  if (size === "small") {
    if (challenge === "ai") recommendationKey = "rec-small-ai";
    else if (challenge === "cyber") recommendationKey = "rec-small-cyber";
    else recommendationKey = "rec-small-telecom";
  } else if (size === "medium") {
    if (challenge === "ai") recommendationKey = "rec-med-ai";
    else if (challenge === "cyber") recommendationKey = "rec-med-cyber";
    else recommendationKey = "rec-med-telecom";
  } else { // large
    if (challenge === "ai") recommendationKey = "rec-large-ai";
    else if (challenge === "cyber") recommendationKey = "rec-large-cyber";
    else recommendationKey = "rec-large-telecom";
  }

  // Get recommended text
  const recommendationText = translations[currentLang][recommendationKey];
  
  // Display result
  const resultCard = document.getElementById("calc-result-card");
  const resultText = document.getElementById("calc-result-text");
  
  if (resultCard && resultText) {
    resultText.textContent = recommendationText;
    resultCard.classList.remove("hidden");
    
    // Scroll result into view smoothly
    resultCard.scrollIntoView({ behavior: "smooth", block: "nearest" });
  }
}

// Contact Form Handler
function handleFormSubmit(e) {
  e.preventDefault();
  
  const submitBtn = document.getElementById("form-submit-btn");
  const successMsg = document.getElementById("form-success-msg");
  
  if (submitBtn && successMsg) {
    // Disable submit button
    submitBtn.disabled = true;
    submitBtn.style.opacity = "0.7";
    
    // Show success message
    successMsg.classList.remove("hidden");
    successMsg.classList.add("fade-in");
    
    // Reset form after a small delay
    setTimeout(() => {
      e.target.reset();
      submitBtn.disabled = false;
      submitBtn.style.opacity = "1";
    }, 4000);
  }
}
